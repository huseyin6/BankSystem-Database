create table BANK (    
    bank_id NUMBER(4),    
    bank_name VARCHAR2(30) NOT NULL,   
    CONSTRAINT bank_id_pk PRIMARY KEY(bank_id));

create table BRANCH (    
    branch_id NUMBER(4),    
    branch_name VARCHAR2(25),    
    branch_city VARCHAR2(25),    
    bank_id NUMBER(4) NOT NULL,    
    CONSTRAINT branch_id_pk PRIMARY KEY(branch_id),    
    CONSTRAINT bank_id_fk FOREIGN KEY(bank_id)    
    REFERENCES bank(bank_id) ON DELETE CASCADE);

create table ACCOUNTT (    
    account_id NUMBER(4),    
    account_name VARCHAR2(25),    
    account_base_amount NUMBER(9),    
    account_interest_rate NUMBER(7,3),   
    branch_id NUMBER(4) NOT NULL,   
    CONSTRAINT account_id_pk PRIMARY KEY(account_id),    
    CONSTRAINT branch_id_fk FOREIGN KEY(branch_id)    
    REFERENCES branch(branch_id) ON DELETE CASCADE);

create table CUSTOMER (    
    customer_id NUMBER(4),    
    customer_name VARCHAR2(50),    
    customer_address VARCHAR2(200),    
    customer_email VARCHAR2(50),    
    account_id NUMBER(4) NOT NULL,   
    withdraw_amount NUMBER(4),   
    CONSTRAINT customer_id_pk PRIMARY KEY(customer_id),    
    CONSTRAINT account_id_fk FOREIGN KEY(account_id)   
    REFERENCES accountt(account_id) ON DELETE CASCADE);

create table CUSTOMER_PHONE (    
    customer_id NUMBER(4),    
    customer_phone_number NUMBER(30),    
    CONSTRAINT customer_phone_number_pk PRIMARY KEY(customer_phone_number),    
    CONSTRAINT customer_id_fk FOREIGN KEY(customer_id)    
    REFERENCES customer(customer_id) ON DELETE CASCADE);

create table EMPLOYEE (    
    employee_id NUMBER(4),    
    employee_name VARCHAR2(50),    
    employee_identity_number NUMBER(15),    
    employee_dept VARCHAR2(20),    
    employee_address VARCHAR2(200),    
    employee_email VARCHAR2(50),    
    employee_gender VARCHAR2(15),    
    branch_id NUMBER(4) NOT NULL,    
    customer_id NUMBER(4) NOT NULL,    
    CONSTRAINT employee_id_pk PRIMARY KEY(employee_id),    
    CONSTRAINT branch_id_fk1 FOREIGN KEY(branch_id)    
    REFERENCES branch(branch_id) ON DELETE CASCADE,    
    CONSTRAINT customer_id_fk2 FOREIGN KEY(customer_id)    
    REFERENCES customer(customer_id) ON DELETE CASCADE);

create table EMPLOYEE_PHONE (    
    employee_id NUMBER(4),    
    employee_phone_number NUMBER(30),    
    CONSTRAINT employee_phone_number_pk PRIMARY KEY(employee_phone_number),    
    CONSTRAINT employee_id_fk1 FOREIGN KEY(employee_id)    
    REFERENCES employee(employee_id) ON DELETE CASCADE);

create table CORPORATE_CUSTOMER(    
    customer_id NUMBER(4),   
    corporate_company_tax_number NUMBER(10),    
    corporate_company_employee VARCHAR2(25),   
    CONSTRAINT customer_id_pk1 PRIMARY KEY(customer_id));

create table RETAIL_CUSTOMER (    
    customer_id NUMBER(4),    
    card_transaction VARCHAR2(30),    
    retail_customer_identity_number NUMBER(11),    
    retail_customer_age NUMBER(3),    
    retail_customer_gender VARCHAR2(15),    
    CONSTRAINT customer_id_pk2 PRIMARY KEY(customer_id));

create table LOAN (    
    loan_id NUMBER(4),    
    loan_name VARCHAR2(25),    
    loan_amount NUMBER(25),    
    branch_id NUMBER(4) NOT NULL,    
    customer_id NUMBER(4) NOT NULL,    
    CONSTRAINT loan_id_pk PRIMARY KEY(loan_id),    
    CONSTRAINT branch_id_fk2 FOREIGN KEY(branch_id)    
    REFERENCES branch(branch_id) ON DELETE CASCADE,    
    CONSTRAINT customer_id_fk3 FOREIGN KEY(customer_id)    
    REFERENCES customer(customer_id) ON DELETE CASCADE);

create table PERSONAL_FINANCE_LOAN (    
    loan_id NUMBER(4),    
    personal_finance_interest_rate_annual NUMBER(5,3),     
    CONSTRAINT loan_id_pk1 PRIMARY KEY(loan_id));

create table HOUSE_LOAN (    
    loan_id NUMBER(4),      
    house_loan_interest_rate_annual NUMBER(5,3),    
    CONSTRAINT loan_id_pk2 PRIMARY KEY(loan_id));

create table VEHICLE_LOAN (    
    loan_id NUMBER(4),    
    vehicle_loan_interest_rate_annual NUMBER(5,3),    
    CONSTRAINT loan_id_pk3 PRIMARY KEY(loan_id));

create table BARROWS (    
    loan_id NUMBER(4) NOT NULL,    
    customer_id NUMBER(4),   
    CONSTRAINT customer_id_pk3 PRIMARY KEY(customer_id),   
    CONSTRAINT loan_id_fk FOREIGN KEY(loan_id)   
    REFERENCES loan(loan_id) ON DELETE CASCADE);

create table DEPOSITT (    
    account_id NUMBER(4),    
    customer_id NUMBER(4) NOT NULL,   
    withdraw_amount NUMBER(4),   
    CONSTRAINT account_id_pk1 PRIMARY KEY(account_id),    
    CONSTRAINT customer_id_fk4 FOREIGN KEY(customer_id)    
    REFERENCES customer(customer_id) ON DELETE CASCADE);

create table SERVE (    
    employee_id NUMBER(4) NOT NULL,    
    customer_id NUMBER(4),   
    CONSTRAINT customer_id_pk4 PRIMARY KEY(customer_id),    
    CONSTRAINT employee_id_fk FOREIGN KEY(employee_id)    
    REFERENCES employee(employee_id) ON DELETE CASCADE);

create table PROVIDES (    
    loan_id NUMBER(4) NOT NULL,    
    branch_id NUMBER(4),   
    CONSTRAINT branch_id_pk2 PRIMARY KEY(branch_id),    
    CONSTRAINT loan_id_fk1 FOREIGN KEY(loan_id)    
    REFERENCES loan(loan_id) ON DELETE CASCADE);

